package pe.edu.lamolina.constant;



public class FormDatoGeneral {

	public Long  codigoTelefonoFijo;
	public Long  codigoTelefonoCelular;
	public String  TextoNumeroTelefonoCelular;
	public String  textoNumeroTelefonoFijo;
	
	
	
	public Long getCodigoTelefonoFijo() {
		return codigoTelefonoFijo;
	}
	public void setCodigoTelefonoFijo(Long codigoTelefonoFijo) {
		this.codigoTelefonoFijo = codigoTelefonoFijo;
	}
	public Long getCodigoTelefonoCelular() {
		return codigoTelefonoCelular;
	}
	public void setCodigoTelefonoCelular(Long codigoTelefonoCelular) {
		this.codigoTelefonoCelular = codigoTelefonoCelular;
	}
	public String getTextoNumeroTelefonoCelular() {
		return TextoNumeroTelefonoCelular;
	}
	public void setTextoNumeroTelefonoCelular(String textoNumeroTelefonoCelular) {
		TextoNumeroTelefonoCelular = textoNumeroTelefonoCelular;
	}
	public String getTextoNumeroTelefonoFijo() {
		return textoNumeroTelefonoFijo;
	}
	public void setTextoNumeroTelefonoFijo(String textoNumeroTelefonoFijo) {
		this.textoNumeroTelefonoFijo = textoNumeroTelefonoFijo;
	}
	
	
	
}
